    private void afficherDessinInstance(){
        
        
        //!!!!!!!!!!!!!!!!!!//
        //!!le probleme ici est que je n'arrive pas a afficher les deux jscroll au meme temps
        //et je n'affiche pas un jscroll avec une bar de scroll
        
        
        
        // creation d'une frame + taille
        JFrame frame = new JFrame("Dessin Instance");
        frame.setPreferredSize(new Dimension(1000, 800));
        //dessin des box
        OptiBoxDessin dB = new OptiBoxDessin(box);
        JScrollPane scroll = new JScrollPane(dB);
        scroll.setHorizontalScrollBar(new JScrollBar());
        scroll.setPreferredSize(new Dimension(900, 200));
        scroll.setAutoscrolls(true);
        scroll.setLocation(1, 1);
        scroll.setAlignmentX(TOP_ALIGNMENT);
        
        
        //dessin des produits
        OptiProduitDessin dP = new OptiProduitDessin(produit);
        JScrollPane scroll2 = new JScrollPane(dP);
        scroll2.setHorizontalScrollBar(new JScrollBar());
        scroll2.setPreferredSize(new Dimension(900, 200));
        scroll2.setAutoscrolls(true);
        scroll.setLocation(1, 500);
        scroll.setAlignmentX(BOTTOM_ALIGNMENT);
        //JLabel label = new JLabel("hello");
        //frame.add(label);
        
        //scroll.add(dB);
        frame.add(scroll);
        frame.add(scroll2);
        frame.pack();
        frame.setVisible(true);
    }
